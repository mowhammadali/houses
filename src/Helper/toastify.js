import { toast } from 'react-toastify';

export const errorNotify = message =>  toast.error( message , {
    position: "top-right",
    autoClose: 2000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: false,
    draggable: false,
    progress: undefined,
    theme: "light",
    });

